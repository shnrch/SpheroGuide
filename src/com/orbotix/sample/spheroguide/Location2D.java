package com.orbotix.sample.spheroguide;

public class Location2D {
	public void Location2D() {
		x=0;
		y=0;
	}
	
    float getX() {
    	return x;
    }
    void setX(float x1) {
    	x = x1;
    }

    float getY() {
    	return y;
    }
    void setY(float y1) {
    	y = y1;
    }

    private float x;
    private float y;
}
